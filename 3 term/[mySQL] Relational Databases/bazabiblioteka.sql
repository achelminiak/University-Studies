DROP DATABASE IF EXISTS biblioteka;
CREATE DATABASE biblioteka;
USE biblioteka;



-- 1. Tabele podstawowe
CREATE TABLE Author(
    Author_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(20) NOT NULL,
    Surname VARCHAR(40) NOT NULL
);

CREATE TABLE Category(
    Category_ID INT AUTO_INCREMENT PRIMARY KEY,
    Category_name VARCHAR(20) NOT NULL
);

CREATE TABLE Publisher(
    Publisher_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(20),
    Release_date DATETIME
);

CREATE TABLE Customer(
    Customer_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(20) NOT NULL,
    Surname VARCHAR(40) NOT NULL,
    Address VARCHAR(40),
    Card_status BOOL
);

CREATE TABLE Worker(
    Worker_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(20) NOT NULL,
    Surname VARCHAR(40) NOT NULL,
    Designation VARCHAR(40)
);

CREATE TABLE On_site(
    On_site_ID INT AUTO_INCREMENT PRIMARY KEY
);

CREATE TABLE Borrow(
    Borrow_ID INT AUTO_INCREMENT PRIMARY KEY,
    Return_date DATETIME
);

CREATE TABLE Parcel_locker(
    Parcel_locker_ID INT AUTO_INCREMENT PRIMARY KEY,
    Return_date DATETIME,
    Locker_number INT
);
SHOW TRIGGERS;	
-- 2. Tabele z kluczami obcymi
CREATE TABLE Book(
    Book_ID INT AUTO_INCREMENT PRIMARY KEY,
    Title VARCHAR(20),
    Available BOOL,
    ISBN CHAR(13),
    Author_ID INT,
    Category_ID INT,
    Publisher_ID INT,
    FOREIGN KEY (Author_ID) REFERENCES Author(Author_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Category_ID) REFERENCES Category(Category_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Publisher_ID) REFERENCES Publisher(Publisher_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE Placement(
    Placement_ID INT AUTO_INCREMENT PRIMARY KEY,
    Rack_number INT,
    Shelf_number INT,
    Category_ID INT,
    FOREIGN KEY (Category_ID) REFERENCES Category(Category_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE Visit(
    Visit_ID INT AUTO_INCREMENT PRIMARY KEY,
    Visit_date DATETIME,
    Customer_ID INT,
    Worker_ID INT,
    FOREIGN KEY (Customer_ID) REFERENCES Customer(Customer_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Worker_ID) REFERENCES Worker(Worker_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

-- 3. Tabele relacyjne
CREATE TABLE VO_s(
    VO_s_ID INT AUTO_INCREMENT PRIMARY KEY,
    On_site_ID INT,
    Visit_ID INT,
    FOREIGN KEY (On_site_ID) REFERENCES On_site(On_site_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Visit_ID) REFERENCES Visit(Visit_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE VB(
    VB_ID INT AUTO_INCREMENT PRIMARY KEY,
    Borrow_ID INT,
    Visit_ID INT,
    FOREIGN KEY (Borrow_ID) REFERENCES Borrow(Borrow_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Visit_ID) REFERENCES Visit(Visit_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE VP_l(
    VP_l_ID INT AUTO_INCREMENT PRIMARY KEY,
    Parcel_locker_ID INT,
    Visit_ID INT,
    FOREIGN KEY (Parcel_locker_ID) REFERENCES Parcel_locker(Parcel_locker_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Visit_ID) REFERENCES Visit(Visit_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE O_sB(
    O_sB_ID INT AUTO_INCREMENT PRIMARY KEY,
    Book_ID INT,
    On_site_ID INT,
    FOREIGN KEY (Book_ID) REFERENCES Book(Book_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (On_site_ID) REFERENCES On_site(On_site_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE BB(
    BB_ID INT AUTO_INCREMENT PRIMARY KEY,
    Book_ID INT,
    Borrow_ID INT,
    FOREIGN KEY (Book_ID) REFERENCES Book(Book_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Borrow_ID) REFERENCES Borrow(Borrow_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE P_lB(
    P_lB_ID INT AUTO_INCREMENT PRIMARY KEY,
    Book_ID INT,
    Parcel_locker_ID INT,
    FOREIGN KEY (Book_ID) REFERENCES Book(Book_ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Parcel_locker_ID) REFERENCES Parcel_locker(Parcel_locker_ID) ON DELETE CASCADE ON UPDATE CASCADE
);

